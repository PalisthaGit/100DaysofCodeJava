// Initialise an ArrayList named weekdays which contains strings from Monday to Sunday
// Print all the elements in the ArrayList weekdays

import java.util.ArrayList;

public class PrintWeekDays {
    public static void main(String[] args) {
        ArrayList weekdays = new ArrayList<>();
        weekdays.add("Sunday");
        weekdays.add("Monday");
        weekdays.add("Tuesday");
        weekdays.add("Wednesday");
        weekdays.add("Thrusday");
        weekdays.add("Friday");
        weekdays.add("Saturday");
        System.out.println(weekdays);
    }

}
